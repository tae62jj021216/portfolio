function renderContact() {
  $("#contactText").textContent = portfolio.contact.text;
  $("#contactActions").innerHTML = portfolio.contact.links
    .map(
      (link) => `
        <a class="button ${link.primary ? "primary" : "secondary"}" href="${link.href}" target="_blank" rel="noreferrer">
          ${link.label}
        </a>
      `,
    )
    .join("");
}

function init() {
  document.title = `${portfolio.name} | Portfolio`;
  $("#brandName").textContent = portfolio.name;
  $("#heroTitle").textContent = portfolio.role;
  $("#heroIntro").textContent = portfolio.intro;
  $("#profileSummary").textContent = portfolio.summary;
  $("#footerName").textContent = `© ${new Date().getFullYear()} ${portfolio.name}`;
  renderStats();
  renderProjects();
  renderSkills();
  renderCredentials();
  renderGantt();
  renderSchedule();
  renderExperiences();
  renderContact();
}

init();
